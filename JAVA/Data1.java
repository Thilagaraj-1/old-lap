class Data1 {
    public static void main(String[] arg) {
        boolean b = true; //
        b = false;
        System.out.println(b);
        double d = 3.14;
        System.out.println(d);
        float f = 3.14F; // float Literal
        System.out.println(f);
        long i = 100L; // long Literal
        System.out.println(i);

        i = 1_00_000;

        System.out.println(i);

    }
}
